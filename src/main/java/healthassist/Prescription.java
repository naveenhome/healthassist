package healthassist;

public class Prescription {
	String patient, doctor, visitDate, vitisTime, fileName, reason;

	public String getPatient() {
		return patient;
	}

	public void setPatient(String patinet) {
		this.patient = patinet;
	}

	public String getDoctor() {
		return doctor;
	}

	public void setDoctor(String doctor) {
		this.doctor = doctor;
	}

	public String getVisitDate() {
		return visitDate;
	}

	public void setVisitDate(String visitDate) {
		this.visitDate = visitDate;
	}

	public String getVitisTime() {
		return vitisTime;
	}

	public void setVitisTime(String vitisTime) {
		this.vitisTime = vitisTime;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

}
